// Helper to get client name by id
function getClientName(id) {
  const client = sampleData.clients.find((c) => c.id === id);
  return client ? client.fullName : "Unknown";
}

// Helper to get driver name by id
function getDriverName(id) {
  if (!id) return "—";
  const driver = sampleData.drivers.find((d) => d.id === id);
  return driver ? driver.name : "Unknown";
}

// Generate next IDs
function nextClientId() {
  return Math.max(...sampleData.clients.map((c) => c.id)) + 1;
}
function nextDriverId() {
  return Math.max(...sampleData.drivers.map((d) => d.id)) + 1;
}
function nextRideId() {
  return Math.max(...sampleData.rides.map((r) => r.id)) + 1;
}
function nextPayoutId() {
  return Math.max(...sampleData.payouts.map((p) => p.id)) + 1;
}
function nextInvoiceNumber() {
  const count = sampleData.invoices.length + 1;
  return `INV-2026-${String(count).padStart(3, "0")}`;
}
